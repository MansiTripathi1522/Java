package Airthmetic;
public class multiple_package {
    public int Add(int a,int b)
    {
        return(a+b);
    }
    public int Sub(int p,int q)
    {
        return(p-q);
    }
    public int Div(int x,int y)
    {
        if(x>y)
    {
        return(x/y);
    }
    else
        {
            return(y/x);
        }
    }
}
